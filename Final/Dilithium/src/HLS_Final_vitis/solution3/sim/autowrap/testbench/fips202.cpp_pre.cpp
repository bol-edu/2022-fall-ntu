static unsigned int keccak_absorb(uint64_t s[25],
                                  unsigned int r,
                                  unsigned int pos,
                                  const uint8_t *m,
                                  size_t mlen)
{
  unsigned int i;
  uint8_t t[8] = {0};

  if(pos & 7) {
    i = pos & 7;
    while(i < 8 && mlen > 0) {
      t[i++] = *m++;
      mlen--;
      pos++;
    }
    s[(pos-i)/8] ^= load64(t);
  }

  if(pos && mlen >= r-pos) {
    for(i=0;i<(r-pos)/8;i++)
      s[pos/8+i] ^= load64(m+8*i);
    m += r-pos;
    mlen -= r-pos;
    pos = 0;
    KeccakF1600_StatePermute(s);
  }

  while(mlen >= r) {
    for(i=0;i<r/8;i++)
      s[i] ^= load64(m+8*i);
    m += r;
    mlen -= r;
    KeccakF1600_StatePermute(s);
  }

  for(i=0;i<mlen/8;i++)
    s[pos/8+i] ^= load64(m+8*i);
  m += 8*i;
  mlen -= 8*i;
  pos += 8*i;

  if(mlen) {
    for(i=0;i<8;i++)
      t[i] = 0;
    for(i=0;i<mlen;i++)
      t[i] = m[i];
    s[pos/8] ^= load64(t);
    pos += mlen;
  }

  return pos;
}
